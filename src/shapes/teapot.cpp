Teapot::Teapot(int partitions, int u_detail, int v_detail, Material* _mat) : material(_mat) {
	
	for (int i = 0; i < TEA_NUMPATCHES; i++) {
		Vector3D vects[4][4];
		for (int j = 0; j < 4; j++) {
			double *verts = teapotVerts[teapotIndices[i][j][0]];
			vects[j][0] = Vector3D(verts[0], verts[1], verts[2]);
			
			verts = teapotVerts[ teapotIndices[i][j][1] ];
			vects[j][1] = Vector3D(verts[0], verts[1], verts[2]);

			verts = teapotVerts[ teapotIndices[i][j][2] ];
			vects[j][2] = Vector3D(verts[0], verts[1], verts[2]);

			verts = teapotVerts[ teapotIndices[i][j][3] ];
			vects[j][3] = Vector3D(verts[0], verts[1], verts[2]);
		}
		patches.push_back(new BezierPatch(&vects, partitions, u_detail, v_detail, material));
	}
	
}


bool Teapot::hit(const Ray& r, double tmin, double tmax, HitRecord& record) const {
	bool is_a_hit = false;
	for (unsigned i = 0; i < patches.size(); i++) {
		if (patches[i]->hit(r, tmin, tmax, record)) {
			tmax = record.t;
			is_a_hit = true;
		}
	}
	return is_a_hit;	
}

bool Teapot::shadowHit(const Ray& r, double tmin, double tmax, Material*& mat) const {
	for (unsigned i = 0; i < patches.size(); i++) {
		if (patches[i]->shadowHit(r, tmin, tmax, mat)) {
			return true;
		}
	}
	return false;
}

